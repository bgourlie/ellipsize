final btnGenerate = query('#generate');
final btnRun = query('#run');
final txtNumElements = query('#numElements');
final divSubjects = query('#subjects');
final txtElapsedTime = query('#elapsedTime');
final spnNumResized = query('#numResized');

void main() {
  btnGenerate.onClick.listen(generate);
  btnRun.onClick.listen(run);
}

void run(Event e){
  int numResized = 0;
  final containers = queryAll('.container');
  final stopwatch = new Stopwatch()..start();
  
  for(final container in containers){
    new Ellipsize(container);
    numResized++;
    window.setImmediate(() {
      spnNumResized.text = numResized.toString();
    });
  }
  
  stopwatch.stop();
  txtElapsedTime.value = '${stopwatch.elapsedMilliseconds}ms';
}

void generate(Event e){

  int numContainers;
  try{
    numContainers = int.parse(txtNumElements.value);
  }catch(ex){
    window.alert('Enter a valid number.');
    return;
  }

  if(numContainers <= 0){
    window.alert('Enter a number greater than 0');
    return;
  }

  final rnd = new Random();
  divSubjects.children.clear();
  for(int i = 0; i < numContainers; i++){
    final container = new DivElement();
    container.classes.add('container');
    final containerHeight = rnd.nextInt(400) + 100;
    container.style.height = '${containerHeight}px';
    final numElements = rnd.nextInt(40) + 10;
    
    for(int y = 0; y < numElements; y++){
      var snippet = snippets[rnd.nextInt(snippets.length - 1)];
      
      var makeList = rnd.nextInt(6) == 5;
      if(makeList){
        var lst = new UListElement();
        
        for(var sentence in snippet.split('\.')){
          if(sentence.trim().isEmpty) continue;
          var li = new LIElement();
          li.text = sentence;
          lst.children.add(li);
        }
        container.children.add(lst);
        continue;
      }
      
      var addBr = rnd.nextInt(6) == 5;
      if(addBr){
        container.children.add(new Element.html('<br>'));
        continue;
      }
      
      final el = new ParagraphElement();
      el.text = snippet;
      container.children.add(el);
    }
    
    divSubjects.children.add(container);
  }
}